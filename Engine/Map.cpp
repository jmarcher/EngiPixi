Map::Map()
{
    this->dirt = TextureManager::LoadTexture("../assets/sprites/dirt.png");
    this->grass = TextureManager::LoadTexture("../assets/sprites/grass.png");
    this->water = TextureManager::LoadTexture("../assets/sprites/water.png");

    this->load(firstLevel);
}

Map::~Map()
{
}

void Map::load(int toLoad[20][25])
{
    for(auto row = 0; row < 20; ++row) {
        for(auto column = 0; column < 25; ++column) {
            map[row][column] = toLoad[row][column];
        }
    }
    this->source.x = this->source.y = 0;
    this->source.w = this->source.h = TILE_SIDE_SIZE;
    this->destination.w = this->destination.h = TILE_SIDE_SIZE * 2;

    this->destination.x = this->destination.y = 0;
}

void Map::draw()
{
    int type = 0;
    for(auto row = 0; row < 20; ++row) {
        for(auto colum = 0; colum < 25; ++colum) {
            type = map[row][colum];
            this->destination.x = row * TILE_SIDE_SIZE * 2;
            this->destination.y = colum * TILE_SIDE_SIZE * 2;
            switch(type) {
            case WATER:
                TextureManager::draw(this->water, this->source, this->destination);
                break;
            case GRASS:
                TextureManager::draw(this->grass, this->source, this->destination);
                break;
            case DIRT:
                TextureManager::draw(this->dirt, this->source, this->destination);
                break;
            default:
                break;
            }
        }
    }
}
