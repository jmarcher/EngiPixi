Map::Map()
{
    this->dirt = TextureManager::LoadTexture("../Data/Sprites/dirt.png");
    this->grass = TextureManager::LoadTexture("../Data/Sprites/grass.png");
    this->water = TextureManager::LoadTexture("../Data/Sprites/water.png");

    this->load(firstLevel);
}

Map::~Map()
{
}

void Map::load(int toLoad[20][25])
{
    for(auto i = 0; i < 20; ++i) {
        for(auto j = 0; j < 25; ++j) {
            map[i][j] = toLoad[i][j];
        }
    }
    this->source.x = this->source.y = 0;
    this->source.w = this->source.h = TILE_SIDE_SIZE;
    this->destination.w = this->destination.h = TILE_SIDE_SIZE * 2 * 2;

    this->destination.x = this->destination.y = 0;
}

void Map::draw()
{
    int type = 0;
    for(auto i = 0; i < 20; ++i) {
        for(auto j = 0; j < 25; ++j) {
            type = map[i][j];
            this->destination.x = i * TILE_SIDE_SIZE * 2;
            this->destination.y = j * TILE_SIDE_SIZE * 2;
            switch(type) {
            case WATER:
                TextureManager::draw(this->water, this->source, this->destination);
                break;
            case GRASS:
                TextureManager::draw(this->grass, this->source, this->destination);
                break;
            case DIRT:
                TextureManager::draw(this->dirt, this->source, this->destination);
                break;
            default:
                break;
            }
        }
    }
}
